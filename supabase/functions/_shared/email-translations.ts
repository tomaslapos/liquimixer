// Funkce pro získání překladu podle jazyka
export function getEmailTranslations(locale: string): EmailTranslations {
  if (!locale) {
    return translations['en']
  }
  
  // Normalizovat locale - zachovat case pro region (ar-SA, zh-CN, etc.)
  const normalizedLocale = locale.replace('_', '-')
  
  // Hledat přesnou shodu (case-sensitive pro kódy jako ar-SA, zh-CN)
  if (translations[normalizedLocale]) {
    return translations[normalizedLocale]
  }
  
  // Zkusit lowercase verzi
  const lowercaseLocale = normalizedLocale.toLowerCase()
  if (translations[lowercaseLocale]) {
    return translations[lowercaseLocale]
  }
  
  // Hledat shodu podle jazyka (např. 'ar' pro 'ar-SA')
  const langCode = normalizedLocale.split('-')[0].toLowerCase()
  if (translations[langCode]) {
    return translations[langCode]
  }
  
  // Speciální případy pro složené kódy
  const specialMappings: Record<string, string> = {
    'ar': 'ar-SA',
    'zh': 'zh-CN',
  }
  if (specialMappings[langCode] && translations[specialMappings[langCode]]) {
    return translations[specialMappings[langCode]]
  }
  
  // Fallback na angličtinu
  return translations['en']
}

// Pomocná funkce pro správný tvar slova "den/dny/dní"
export function getDayWord(days: number, locale: string): string {
  const t = getEmailTranslations(locale)
  
  if (days === 1) {
    return t.day
  } else if (days >= 2 && days <= 4) {
    return t.days_2_4
  } else {
    return t.days_5_plus
  }
}

// Funkce pro nahrazení placeholderů v textu
export function formatEmailText(text: string, replacements: Record<string, string | number>): string {
  let result = text
  for (const [key, value] of Object.entries(replacements)) {
    result = result.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value))
  }
  return result
}
